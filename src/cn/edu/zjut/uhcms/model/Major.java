 package cn.edu.zjut.uhcms.model;

public class Major {
	private String mno;
	public String getMno() {
		return mno;
	}
	public void setMno(String mno) {
		this.mno = mno;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public String getMcollege_no() {
		return mcollege_no;
	}
	public void setMcollege_no(String mcollege_no) {
		this.mcollege_no = mcollege_no;
	}
	private String mname;
	private String mcollege_no;
}
